namespace DemoClassLib
{
    public class MyMathClass
    {
        //sum total func
        public int sum(int i, int j)
        {
            return i + j;
        }

        //multiply func
        public double multi(int i, int j)
        {
            return i * j;
        }

        //substruct
        public int sub(int i, int j)
        {
            return i - j;

        }

    }
}
